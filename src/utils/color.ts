export function applyHue(hue: number, el: HTMLElement): void {
  let overIndex = tailwindColors.findIndex((c) => c.hue >= hue);
  if (overIndex === -1) overIndex = 0;
  const underIndex =
    overIndex === 0 ? tailwindColors.length - 1 : overIndex - 1;
  let overHue = tailwindColors[overIndex].hue;
  if (overHue < hue) overHue += 360;
  let underHue = tailwindColors[underIndex].hue;
  if (underHue > hue) underHue -= 360;
  const percent = (hue - overHue) / (overHue - underHue);

  tailwindColors[overIndex].variants.forEach((v, i) => {
    const [l, c, h] = v.map((w, j) => {
      let underV = tailwindColors[underIndex].variants[i][j];
      if (j === 2 && underV > w) underV -= 360;
      return Number((w + percent * (w - underV)).toPrecision(j === 2 ? 6 : 3));
    });
    el.style.setProperty(`--brand-${weight[i]}`, `oklch(${l} ${c} ${h})`);
  });
}
